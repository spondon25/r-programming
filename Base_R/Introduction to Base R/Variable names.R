#Variable names

#In R, variable names are case sensitive, and a valid variable name (syntactic name) consists of a combination of

#(a-z, A-Z), (0-9), (.), (-), (_)
#A variable name cannot start with a number or a hyphen or an underscore and can start with a dot, but it must be followed by a letter
#A non-syntactic name should be around backticks

#A good programming practice is to use meaningful variable names in the codes, and self-explanatory variable names increase the readability of the codes
#Existing R functions, names, or words (e.g. mean log, exp, TRUE, c, etc.) should not be used as variable names