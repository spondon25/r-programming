# Data type

# There are three major data types in R
# numeric
# (quantitative data)

# Numeric/double 
xn <- 10
# Integer
xi <- 10L

# character
# (qualitative data)
xc <- "10"

# logical
# binary data
xl <- TRUE

# The function typeof(x) can be used to identify the type of an R object x
typeof(xl)
# [1] "logical"
typeof(xn)
# [1] "double"
typeof(xi)
# [1] "integer"
